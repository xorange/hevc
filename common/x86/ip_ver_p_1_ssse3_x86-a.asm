%endmacro

%macro X265_IP_FILTER_N_ROW_ONE_COL_VER_P_HELP_SSSE3 5
	%define COL_SIZE %5
	INIT_REF_HEADER_PIXEL %1, %2, %3, %4
	MOV row, height_parameter
%if X265_LUMA_FLAGS == CHROMA_FLAGS
	X265_IP_FILTER_N_GROUP_ROW_ONE_COL_VER_P_HELP_SSSE3 %1, %2, %3, %4, 4
	X265_IP_FILTER_N_GROUP_ROW_ONE_COL_VER_P_HELP_SSSE3 %1, %2, %3, %4, 3
	X265_IP_FILTER_N_GROUP_ROW_ONE_COL_VER_P_HELP_SSSE3 %1, %2, %3, %4, 2
	X265_IP_FILTER_N_GROUP_ROW_ONE_COL_VER_P_HELP_SSSE3 %1, %2, %3, %4, 1
%elif X265_CHROMA_FLAGS == CHROMA_FLAGS
	X265_IP_FILTER_N_GROUP_ROW_ONE_COL_VER_P_HELP_SSSE3 %1, %2, %3, %4, 2
	X265_IP_FILTER_N_GROUP_ROW_ONE_COL_VER_P_HELP_SSSE3 %1, %2, %3, %4, 1
%endif
%endmacro

%macro X265_IP_FILTER_N_ROW_N_COL_VER_P_HELP_SSSE3		5
	CMP col, MAX_COL_SIZE
	JL %%X265_IP_FILTER_N_ROW_N_COL_VER_P_HELP_SSSE3_LABEL_LESS_THAN_MAX_COL_SIZE
%%X265_IP_FILTER_N_ROW_N_COL_VER_P_HELP_SSSE3_LABEL_MAX_COL_SIZE_LOOP:
	SET_REF_AND_DST
	X265_IP_FILTER_N_ROW_ONE_COL_VER_P_HELP_SSSE3 %1, %2, %3, %4, MAX_COL_SIZE
	SUB col, MAX_COL_SIZE
	CMP col, MAX_COL_SIZE
	JGE %%X265_IP_FILTER_N_ROW_N_COL_VER_P_HELP_SSSE3_LABEL_MAX_COL_SIZE_LOOP
%%X265_IP_FILTER_N_ROW_N_COL_VER_P_HELP_SSSE3_LABEL_LESS_THAN_MAX_COL_SIZE
%if %5 > 0
	SET_REF_AND_DST
	X265_IP_FILTER_N_ROW_ONE_COL_VER_P_HELP_SSSE3 %1, %2, %3, %4, %5
	SUB col, %5
%endif
%endmacro

%macro RUN_AVAILABLE_IP_FILTER_N_ROW_N_COL_VER_P_FUNC 1
	MOV r1, 0
	CMP r0, %1
	JNE %%RUN_AVAILABLE_IP_FILTER_N_ROW_N_COL_VER_P_FUNC_EXIT
	X265_IP_FILTER_N_ROW_N_COL_VER_P_HELP_SSSE3 XMMR1, XMMR2, XMMR3, XMMR4, %1
	MOV r1, 1
%%RUN_AVAILABLE_IP_FILTER_N_ROW_N_COL_VER_P_FUNC_EXIT:
%endmacro

%macro X265_IP_FILTER_VER_P_SSSE3_HELP 4
	%define			XCOEFFS						%2
	%define			CHROMA_FLAGS				%3
	%define			DST_FLAGS					%4
	%define			SPACE1						r0
	%define			SPACE2						r2
	%define			SPACE3						r3
	%define			col							r6
	%define			row							r5
	%define			ref							r4
	%define			dst							r3
	%define			dst_int						r1
	%define			XSPACE1						XMMR0
	%define			XSPACE2						XMMR7
	%define			XRESULT						XSPACE2
	%define			XMAX						XMMR5
	%define			XOFFSET_AND_SHIFT			XMMR6
	%define			ref_parameter				r0m
	%define			ref_stride_parameter		r1m
	%define			dst_parameter				r2m
	%define			dst_stride_parameter		r3m
	%define			width_parameter				r4m
	%define			height_parameter			r5m
	%define			bit_depth_parameter			r6m
	%define			dst_int_parameter			r7m
%if X265_LUMA_FLAGS == CHROMA_FLAGS
	%define			XCOEFF1						[XCOEFFS     ]
	%define			XCOEFF2						[XCOEFFS+16*1]
	%define			XCOEFF3						[XCOEFFS+16*2]
	%define			XCOEFF4						[XCOEFFS+16*3]
%elif X265_CHROMA_FLAGS == CHROMA_FLAGS
	%define			XCOEFF1						XMMR3
	%define			XCOEFF2						XMMR4
%endif
	%define			MAX_COL_SIZE				8
	%define			MAX_COL_SIZE_BITS			3
cglobal %1, 0, 7
	PREPARE_PARAMETER
	MOV col, width_parameter
	MOV r0, col
	MOV r1, col
	SHR r1, MAX_COL_SIZE_BITS
	SHL r1, MAX_COL_SIZE_BITS
	SUB r0, r1
	RUN_AVAILABLE_IP_FILTER_N_ROW_N_COL_VER_P_FUNC 0
	CMP r1, 1
	JE %%X265_IP_FILTER_VER_P_SSSE3_HELP_EXIT
	RUN_AVAILABLE_IP_FILTER_N_ROW_N_COL_VER_P_FUNC 4
	CMP r1, 1
	JE %%X265_IP_FILTER_VER_P_SSSE3_HELP_EXIT
	RUN_AVAILABLE_IP_FILTER_N_ROW_N_COL_VER_P_FUNC 2
	CMP r1, 1
	JE %%X265_IP_FILTER_VER_P_SSSE3_HELP_EXIT
	RUN_AVAILABLE_IP_FILTER_N_ROW_N_COL_VER_P_FUNC 6
	CMP r1, 1
	JE %%X265_IP_FILTER_VER_P_SSSE3_HELP_EXIT
	RUN_AVAILABLE_IP_FILTER_N_ROW_N_COL_VER_P_FUNC 1
	CMP r1, 1
	JE %%X265_IP_FILTER_VER_P_SSSE3_HELP_EXIT
	RUN_AVAILABLE_IP_FILTER_N_ROW_N_COL_VER_P_FUNC 3
	CMP r1, 1
	JE %%X265_IP_FILTER_VER_P_SSSE3_HELP_EXIT
%%X265_IP_FILTER_VER_P_SSSE3_HELP_EXIT:
	RET
%endmacro

